import os
import sys

def print_progress_bar(iteration, total, prefix='', suffix='', decimals=1, length=100, fill='█'):
    """
    Call in a loop to create terminal progress bar
    @params:
        iteration   - Required  : current iteration (Int)
        total       - Required  : total iterations (Int)
        prefix      - Optional  : prefix string (Str)
        suffix      - Optional  : suffix string (Str)
        decimals    - Optional  : positive number of decimals in percent complete (Int)
        length      - Optional  : character length of bar (Int)
        fill        - Optional  : bar fill character (Str)
    """
    percent = ("{0:." + str(decimals) + "f}").format(100 * (iteration / float(total)))
    filled_length = int(length * iteration // total)
    bar_str = fill * filled_length + '　' * (length - filled_length)
    print('\r%s |%s| %s%% %s' % (prefix, bar_str, percent, suffix), end='\r', file=sys.stderr)
    # Print New Line on Complete
    if iteration == total:
        print(file=sys.stderr)

def fa_stats(fa_file, bin_size=10):
    """Prints fasta file stats
    fa_file: string path to fasta file.
    bin_size: bin size for sequence length histogram
    """
    fa_f = open(fa_file, 'r')
    file_size = os.path.getsize(fa_file)

    seq = ''
    len_list = []
    hist_list = []
    bytes_read = 0

    def process_seq():
        """Record sequence length
        """
        nonlocal hist_list
        if seq: # closure seq
            seq_len = len(seq)
            seq_bin = int(seq_len / bin_size)
            len_list.append(seq_len)
            # extend hist_list if needed
            if seq_bin + 1 > len(hist_list):
                hist_list += [0] * (seq_bin + 1 - len(hist_list))
            hist_list[seq_bin] += 1
            if len(len_list) % 100000 == 0:
                print_progress_bar(bytes_read, file_size, prefix='Progress:', suffix='Complete', length=50)
    
    print_progress_bar(bytes_read, file_size, prefix='Progress:', suffix='Complete', length=50)
    for line in fa_f:
        bytes_read += len(line)
        line = line.strip()
        if len(line) > 0 and line[0] == '>':
            process_seq()
            seq = ''
        else:
            seq += line
    process_seq() # last seq
    print_progress_bar(file_size, file_size, prefix='Progress:', suffix='Complete', length=50)
    fa_f.close()

    # output
    print('{}\t{}'.format('Bin', 'Count'))
    for i, c in enumerate(hist_list):
        print('{}\t{}'.format(i * bin_size, c))
    len_list.sort()
    print('{:>20}{:>15}'.format('Sequence Count:', len(len_list)))
    print('{:>20}{:>15}'.format('Min Length:', len_list[0]))
    print('{:>20}{:>15}'.format('Median Length:', len_list[int(len(len_list)/2)]))
    print('{:>20}{:>15}'.format('Average Length:', int(sum(len_list)/len(len_list))))
    print('{:>20}{:>15}'.format('Max Length:', len_list[-1]))


